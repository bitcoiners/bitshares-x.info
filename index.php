                    <div class="item">
                        <h3 class="question"><i class="fa fa-question-circle"></i>There is no real Gold backing BitGold. So how does it have value ?</h3>
                        <p class="answer">BitAssets can track the price of any commodity with enough market participants. BitGold, like any BitAsset, will require market participants to take opposing positions on the future price of that BitAsset. If everyone expects BitGold to rise, then market participants will buy and push the price up until there are participants willing to short. As long as everyone thinks that everyone else predicts that the consensus will hold, then the consensus will continue to hold and reward those who believe it will. Thus it creates a self-reinforcing price peg.</p>
                    </div><!--//item-->

		   <div class="item">
                        <h3 class="question"><i class="fa fa-question-circle"></i>What kind of asset can be market-pegged ?</h3>
